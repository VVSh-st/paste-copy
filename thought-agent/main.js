// src/utils/cosine.ts
function cosineSimilarity(a, b) {
  let dot = 0;
  let normA = 0;
  let normB = 0;
  for (let i = 0; i < a.length; i++) {
    dot += a[i] * b[i];
    normA += a[i] * a[i];
    normB += b[i] * b[i];
  }
  if (normA === 0 || normB === 0) return 0;
  return dot / (Math.sqrt(normA) * Math.sqrt(normB));
}
function normalizeScores(scores) {
  const min = Math.min(...scores);
  const max = Math.max(...scores);
  if (max === min) return scores.map(() => 0);
  return scores.map((s) => (s - min) / (max - min));
}

// src/retrieval/BM25.ts
function tokenize2(text) {
  return text.toLowerCase().replace(/[^\p{L}\p{N}\s]/gu, " ").split(/\s+/).filter((t) => t.length > 1);
}
function termFreq(term, tokens) {
  return tokens.filter((t) => t === term).length;
}
function bm25(query, chunks, k1 = 1.5, b = 0.75) {
  const queryTerms = tokenize2(query);
  if (queryTerms.length === 0)
    return chunks.map((c) => ({ chunk: c, score: 0 }));
  const tokenizedChunks = chunks.map((c) => tokenize2(c.content));
  const avgDocLen = tokenizedChunks.reduce((s, t) => s + t.length, 0) / (tokenizedChunks.length || 1);
  const df = {};
  for (const term of queryTerms) {
    df[term] = tokenizedChunks.filter((tokens) => tokens.includes(term)).length;
  }
  return chunks.map((chunk, i) => {
    const tokens = tokenizedChunks[i];
    const docLen = tokens.length;
    let score = 0;
    for (const term of queryTerms) {
      const tf = termFreq(term, tokens);
      if (tf === 0) continue;
      const idf = Math.log(
        (chunks.length - df[term] + 0.5) / (df[term] + 0.5) + 1
      );
      const norm = 1 - b + b * (docLen / avgDocLen);
      score += idf * (tf * (k1 + 1)) / (tf + k1 * norm);
    }
    return { chunk, score };
  });
}

// src/retrieval/HybridSearch.ts
function hybridSearch(queryEmbedding, queryText, chunks, topK = 10) {
  if (chunks.length === 0) return [];
  if (queryEmbedding.length === 0) {
    const bm25Results2 = bm25(queryText, chunks);
    bm25Results2.sort((a, b) => b.score - a.score);
    return bm25Results2.slice(0, topK);
  }
  const chunksWithEmbeddings = chunks.filter((c) => c.embedding && c.embedding.length > 0);
  if (chunksWithEmbeddings.length === 0) {
    const bm25Results2 = bm25(queryText, chunks);
    bm25Results2.sort((a, b) => b.score - a.score);
    return bm25Results2.slice(0, topK);
  }
  const semanticRaw = chunksWithEmbeddings.map((c) => cosineSimilarity(queryEmbedding, c.embedding));
  const semanticNorm = normalizeScores(semanticRaw);
  const bm25Results = bm25(queryText, chunksWithEmbeddings);
  const bm25Raw = bm25Results.map((r) => r.score);
  const bm25Norm = normalizeScores(bm25Raw);
  const combined = chunksWithEmbeddings.map((chunk, i) => ({
    chunk,
    score: 0.7 * semanticNorm[i] + 0.3 * bm25Norm[i]
  }));
  combined.sort((a, b) => b.score - a.score);
  return combined.slice(0, topK);
}

