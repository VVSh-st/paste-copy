function graphEnhancedRetrieval(app, results, queryEmbedding, store, neighborPenalty = 0.6) {
  const seenPaths = new Set(results.map((r) => r.chunk.notePath));
  const neighborChunks = [];
  const resolvedLinks = app.metadataCache.resolvedLinks ?? {};
  const getBacklinkPaths = (notePath) => {
    const paths = [];
    for (const [sourcePath, targets] of Object.entries(resolvedLinks)) {
      if (targets && Object.prototype.hasOwnProperty.call(targets, notePath)) {
        paths.push(sourcePath);
      }
    }
    return paths;
  };
  for (const result of results) {
    const file = app.vault.getFileByPath(result.chunk.notePath);
    if (!file) continue;
    const cache = app.metadataCache.getFileCache(file);
    if (!cache) continue;
    const linkedPaths = /* @__PURE__ */ new Set();
    if (cache.links) {
      for (const link of cache.links) {
        const resolved = app.metadataCache.getFirstLinkpathDest(
          link.link,
          file.path
        );
        if (resolved) linkedPaths.add(resolved.path);
      }
    }
    for (const path of getBacklinkPaths(file.path)) {
      linkedPaths.add(path);
    }
    for (const linkedPath of linkedPaths) {
      if (seenPaths.has(linkedPath)) continue;
      seenPaths.add(linkedPath);
      const summaryChunks = store.getChunksForNote(linkedPath).filter((c) => c.level === 1);
      for (const chunk of summaryChunks) {
        if (!chunk.embedding || chunk.embedding.length === 0) continue;
        const sim = cosineSimilarity(queryEmbedding, chunk.embedding);
        neighborChunks.push({ chunk, score: sim * neighborPenalty });
      }
    }
  }
  return [...results, ...neighborChunks];
}

// src/retrieval/MMR.ts
function mmr(candidates, finalK, lambda = 0.7) {
  if (candidates.length === 0) return [];
  const selected = [];
  const remaining = [...candidates];
  while (selected.length < finalK && remaining.length > 0) {
    let bestIdx = 0;
    let bestScore = -Infinity;
    for (let i = 0; i < remaining.length; i++) {
      const candidate = remaining[i];
      const relevance = candidate.score;
      let redundancy = 0;
      if (selected.length > 0) {
        redundancy = Math.max(
          ...selected.map(
            (s) => cosineSimilarity(candidate.chunk.embedding, s.chunk.embedding)
          )
        );
      }
      const mmrScore = lambda * relevance - (1 - lambda) * redundancy;
      if (mmrScore > bestScore) {
        bestScore = mmrScore;
        bestIdx = i;
      }
    }
    selected.push(remaining[bestIdx]);
    remaining.splice(bestIdx, 1);
  }
  return selected;
}

